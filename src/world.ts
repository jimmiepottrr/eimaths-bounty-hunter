export type SceneDef = {
  scene: number; // 1..4 (ส่งให้ API ตรงๆ)
  name: string;
  ping: string; // บทพูดปิ๊งตอนเข้าฉาก
  art: string; // slug อาร์ตฉาก (stylized 3D) — โหลดผ่าน artUrl()
};

export type BossDef = {
  name: string;
  title: string;
  quote: string; // บทเปิดตัวบอส
  clip: string; // สคริปต์/คำโปรยคลิปหลังชนะ (เฟสนี้ใช้ placeholder)
  video: string; // ไฟล์ placeholder cutscene (ไม่มีนามสกุล — โค้ดเติม .h265.mp4 / .h264.mp4)
  poster: string;
  art: string; // slug อาร์ตบอส (stylized 3D) — โหลดผ่าน artUrl()
};

export type LandDef = {
  grade: number; // 3..6
  gradeLabel: string; // ป.3 ...
  land: string;
  emoji: string;
  theme: 'dawn' | 'desert' | 'ice' | 'shadow';
  timeLimitSec: number; // ค่าแสดงผลเบื้องต้น — ค่าจริงใช้ time_limit_sec จาก API เสมอ
  scenes: SceneDef[];
  boss: BossDef;
};

