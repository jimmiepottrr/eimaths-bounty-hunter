export const getWorld = (grade: number): LandDef =>
  WORLDS.find((world) => world.grade === grade) ?? WORLDS[0];

/** ฉากสุดท้ายก่อนบอสของแต่ละชั้น (ป.6 = 3 ฉาก, ชั้นอื่น = 4 ฉาก) */
export const lastSceneOf = (world: LandDef) => world.scenes.length;

/** slug อาร์ตของฉาก (คืน undefined ถ้าเลข scene ไม่ถูกต้อง) */
export const sceneArtSlug = (grade: number, scene: number): string | undefined =>
  getWorld(grade).scenes[scene - 1]?.art;

/** slug อาร์ตของบอสประจำดินแดน */
export const bossArtSlug = (grade: number): string => getWorld(grade).boss.art;
